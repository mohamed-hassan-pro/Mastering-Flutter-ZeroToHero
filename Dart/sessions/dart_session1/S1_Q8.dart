/*8. Write a Dart program that declares two numeric variables and performs addition, subtraction,
multiplication, division, and modulo on them. Print each result.*/
void main() {
  num a = 6.5;
  num b = 3;
  num addition = a + b;
  num subtracting = a - b;
  num multiplication = a * b;
  num division = a / b;
  num modulo = a % b;
  print("The addition of $a and $b is: $addition");
  print("The subtraction of $a and $b is: $subtracting");
  print("The multiplication of $a and $b is: $multiplication");
  print("The division of $a and $b is: $division");
  print("The modulo of $a and $b is: $modulo");
}
